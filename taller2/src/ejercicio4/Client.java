package ejercicio4;

public class Client {
    private String name, numId, company;

    public Client(String name, String numId, String company) {
        this.name = name;
        this.numId = numId;
        this.company = company;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumId() {
        return numId;
    }

    public void setNumId(String numId) {
        this.numId = numId;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }




    
}
